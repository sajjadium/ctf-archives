	if sign == "sign"{
		sign = c.Params.Form["sign"][0]
	}
	if sign == "signs"{
		return c.RenderJSON(signList)
	}else if signList.Has(sign){
		iconurl := icons[sign]
		return c.Render(sign, horoscope, iconurl)
	}else{
		c.Flash.Error("Invalid sign provided: " + sign)
		c.FlashParams()
		return c.Redirect("/")
	}

